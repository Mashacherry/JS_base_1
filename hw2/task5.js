// ### 5. Реализовать основные 4 арифметические операции в виде функций с двумя параметрами. Обязательно использовать оператор return. 
function func_summ(a, b) {
    return (a + b);
}

function func_subt(a, b) {
    return (a - b);
}

function func_div(a, b) {
    return (a / b);
}

function func_mult(a, b) {
    return (a * b);
}